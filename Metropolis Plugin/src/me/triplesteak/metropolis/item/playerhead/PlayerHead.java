	PlayerHead(String playerUUID, String textureCode) {
		ItemStack is = new ItemStack(Material.PLAYER_HEAD, 1);
		net.minecraft.server.v1_16_R3.ItemStack nmsStack = CraftItemStack.asNMSCopy(is);

		NBTTagCompound compound = new NBTTagCompound();
		nmsStack.setTag(compound);
		compound = nmsStack.getTag();

		String noHyphen = playerUUID.replace("-", "");
		int[] intList = new int[4];
		for (int i = 0; i < 4; i++)
			intList[i] = (int) Long.parseLong(noHyphen.substring(i * 8, i * 8 + 8), 16);

		NBTTagCompound skullOwner = new NBTTagCompound();
		NBTTagIntArray uuidIntList = new NBTTagIntArray(intList);
		skullOwner.set("Id", uuidIntList);
		NBTTagCompound properties = new NBTTagCompound();
		NBTTagList textures = new NBTTagList();
		NBTTagCompound value = new NBTTagCompound();
		value.set("Value", NBTTagString.a(textureCode));
		textures.add(value);
		properties.set("textures", textures);
		skullOwner.set("Properties", properties);

		compound.set("SkullOwner", skullOwner);
		nmsStack.setTag(compound);

		this.is = CraftItemStack.asBukkitCopy(nmsStack);
	}

